    private void btnTCMoveRightActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTCMoveRightActionPerformed

        ListModel leftLM = lstMTestCasesLeft.getModel();
        ListModel rightLM = lstMTestCasesRight.getModel();

        DefaultListModel leftDLM = new DefaultListModel();  
        DefaultListModel rightDLM = new DefaultListModel(); 

        for(int i=0;i<leftLM.getSize();i++){
            if(lstMTestCasesLeft.isSelectedIndex(i)){  
                rightDLM.addElement(leftLM.getElementAt(i));
            }//if
            else{
                leftDLM.addElement(leftLM.getElementAt(i)); 
            }//else
        }//for
        lstMTestCasesLeft.setModel(leftDLM); 
        System.out.println(leftDLM); 

        for(int i=0;i<rightLM.getSize();i++){
            rightDLM.addElement(rightLM.getElementAt(i));
        }

        lstMTestCasesRight.setModel(rightDLM);

        lstMTestCasesLeft.clearSelection();
        lstMTestCasesLeft.clearSelection();
    }//GEN-LAST:event_btnTCMoveRightActionPerformed

    private void btnTCMoveLeftActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTCMoveLeftActionPerformed

        //get access through interface ListModel to left and Right JLists's models
        ListModel leftLM = lstMTestCasesLeft.getModel();
        ListModel rightLM = lstMTestCasesRight.getModel();

        DefaultListModel leftDLM = new DefaultListModel();  //Create new instance of DefaultListModel for left JList
        DefaultListModel rightDLM = new DefaultListModel(); //Create new instance of DefaultListModel for right JList

        //copy existing items to DefaultListModel
        for(int i=0;i<rightLM.getSize();i++){
            if(lstMTestCasesRight.isSelectedIndex(i)){  //but we have to skip selected item
                leftDLM.addElement(rightLM.getElementAt(i));
            }//if
            else{
                rightDLM.addElement(rightLM.getElementAt(i)); //element added
            }//else
        }//for
        lstMTestCasesRight.setModel(rightDLM);//set new model for left JList without "deleted" item
        //System.out.println(leftDLM);//let's print it and see the new model

        //copy items from right ListModel to new DefaultListModel
        for(int i=0;i<leftLM.getSize();i++){
            leftDLM.addElement(leftLM.getElementAt(i));
        }

        lstMTestCasesLeft.setModel(leftDLM);

        lstMTestCasesLeft.clearSelection();
        lstMTestCasesLeft.clearSelection();
    }//GEN-LAST:event_btnTCMoveLeftActionPerformed

    private void lstMTestCasesRightMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstMTestCasesRightMouseClicked
       JList list = (JList)evt.getSource();
        if (evt.getClickCount() == 2) {
            int index = list.locationToIndex(evt.getPoint());
            if (index >= 0) {
                Object selItem = list.getModel().getElementAt(index);
                ((DefaultListModel) lstMTestCasesLeft.getModel()).addElement(selItem);
                ((DefaultListModel) lstMTestCasesRight.getModel()).removeElement(selItem);
             }
        }
    }//GEN-LAST:event_lstMTestCasesRightMouseClicked

