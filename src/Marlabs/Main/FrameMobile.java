    static DefaultListModel sort(DefaultListModel dlm) { 
        Object [] dlma = dlm.toArray();   
        Arrays.sort(dlma);  
        dlm.clear();   
        for (Object x : dlma)
            dlm.addElement(x);   
        
        return dlm;
    }
    
    public void LoadTestCases()
    {
       String  suiteName = Constants.MobileSuiteName;
       String  path = Constants.MobileTestSuite;
       String  sheetName = Constants.MobileSuiteSheetName;
      
        Xls_Reader excel = new Xls_Reader(path);
        int cellId = 2;
        String cellData = "";
        
        DefaultListModel mTestCases = new DefaultListModel();
        DefaultListModel mTestCasesR = new DefaultListModel();
        lstMTestCasesRight.setModel(mTestCasesR);
        while(true)
        {
           cellData = (String) excel.getCellData(sheetName, 0, cellId);
         
           if(cellData.trim().length() > 0)
           {
               mTestCases.addElement(cellData); 
               cellId++;
           }
           else
               break;
        }
        
        lstMTestCasesLeft.setModel(sort(mTestCases));
    }

